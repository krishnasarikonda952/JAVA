class Multiplication{
	void mul2Numbers(int firstNum, int secondNum){
		int result=firstNum*secondNum;
		
		System.out.println(result);
	}
	
	void mul3Numbers(int firstNum, int secondNum, int thirdNum){
		int result=firstNum*secondNum*thirdNum;
		
		System.out.println((long)result);
	}
	
	void mul4Numbers(int firstNum, int secondNum, int thirdNum, int fourthNum){
		int result=firstNum*secondNum*thirdNum*fourthNum;
		
		System.out.println((long)result);
	}
	
}