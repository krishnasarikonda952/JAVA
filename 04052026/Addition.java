class Addition{
	void add2Numbers(int firstNum, int secondNum){
		int result=firstNum+secondNum;
		
		System.out.println(result);
	}
	
	void add3Numbers(int firstNum, int secondNum, int thirdNum){
		int result=firstNum+secondNum+thirdNum;
		
		System.out.println(result);
	}
	
	void add4Numbers(int firstNum, int secondNum, int thirdNum, int fourthNum){
		int result=firstNum+secondNum+thirdNum+fourthNum;
		
		System.out.println(result);
	}
	
}